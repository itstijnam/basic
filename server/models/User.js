
import mongoose from 'mongoose';
import { type } from 'os';

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    trim: true
  },
  email: {
    type: String,
    required: true,
    unique: true,
    lowercase: true
  },
  password: {
    type: String,
    required: true
  },
  role: {
    type: String,
    enum: ['admin', 'user'],
    default: 'user'
  },
  secret_key: { type: String }
}, {
  timestamps: true
});

export const User = mongoose.model('User', userSchema);